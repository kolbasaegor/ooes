void half_triangle(int n){
  for(int i = 0; i < n; i++){
    for(int j = 0; j < i+1; j++)
      printf("*");
    printf("\n");
  }
}

void rectangle(int n, int m){ 
  for(int i = 0; i < n; i++){
    for(int j = 0; j < m; j++)
      printf("*");
    printf("\n");
  }
}
